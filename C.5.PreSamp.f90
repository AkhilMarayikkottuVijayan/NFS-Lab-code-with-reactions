SUBROUTINE SAMPINIT
USE VARIABLES
INTEGER :: Q
ALLOCATE(CSS(7,1:TNS),CSS_L(7,1:TNS),CSSP(7,1:TNS,1:NSP_T))
ALLOCATE(CS(12,1:NC_MAX,1:NSP_T),COL(1:NSP_T,1:NSP_T))
ALLOCATE(CSNL(14,1:NC_MAX,1:NSP_T),CSN(14,NC_MAX))
ALLOCATE(TVIB(1:NC_MAX,1:NSP_T),SVIB(1:NC_MAX,1:NSP_T))
ALLOCATE(BIN(3,1:NBINS))
	DO 100 L=1,NSP_T
		DO 110 N=1,NC_MAX !NC_D
			SVIB(N,L)=0
			TVIB(N,L)=0

			CS(1,N,L)=1.0E-6

			DO 120 Q=2,12
				CS(Q,N,L)=0.0d0
120 			CONTINUE
110		CONTINUE
100     CONTINUE

	DO 200 L=1,NSP_T
		DO 210 LL=1,NSP_T
			COL(L,LL)=0
210	CONTINUE
200 CONTINUE

	 DO 300 K=1,TNS
		DO 310 Q=1,7
			CSS(Q,K)=0.
310  CONTINUE
300 CONTINUE

	DO 400 L=1,NSP_T
		DO 410 K=1,TNS
			DO 420 Q=1,7
				CSSP(Q,K,L)=0
420		CONTINUE
410	CONTINUE
400 CONTINUE

DO 430 I=1,TNS
		QI(I)=0
		QR(I)=0
		Q_I(I)=0
		Q_R(I)=0
430 CONTINUE
RETURN
END SUBROUTINE

